<p>O <b>ODS 12</b> da ONU exige um gerenciamento ecologicamente racional. A solução reside em reverter o modelo de
    consumo:
</p>

<div class="row">
    <div class="col-md-4">
        <div class="card h-100 p-3 shadow-sm border-primary">
            <h6>1. Reduzir 📉</h6>
            <p>Seja menos permeável às estratégias de marketing. A mudança depende do usuário e dos fabricantes, que
                devem optar por <b>design ecológico</b> e durabilidade.</p>
        </div>
    </div>
    <div class="col-md-4">
